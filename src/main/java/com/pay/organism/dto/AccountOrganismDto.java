package com.pay.organism.dto;

public class AccountOrganismDto {
    String accountName;
    String accountEmail;
    String accountPassword;
    String accountRole;
    String organismDesign;
    String organismBudget;
    
    public String getAccountName() {
        return accountName;
    }
    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }
    public String getAccountEmail() {
        return accountEmail;
    }
    public void setAccountEmail(String accountEmail) {
        this.accountEmail = accountEmail;
    }
    public String getAccountPassword() {
        return accountPassword;
    }
    public void setAccountPassword(String accountPassword) {
        this.accountPassword = accountPassword;
    }
    public String getAccountRole() {
        return accountRole;
    }
    public void setAccountRole(String accountRole) {
        this.accountRole = accountRole;
    }
    public String getOrganismDesign() {
        return organismDesign;
    }
    public void setOrganismDesign(String organismDesign) {
        this.organismDesign = organismDesign;
    }
    public String getOrganismBudget() {
        return organismBudget;
    }
    public void setOrganismBudget(String organismBudget) {
        this.organismBudget = organismBudget;
    }
    
    
}
